# 2. 실수형: 소수점이 있는 수
print(1.1, 3.1, 0, -1.4)
# - 문자열 자료형
# "" or ''
print('python')
print("python is fun!", end = ",") #문장 사이의 공백을 없애주거나 구분자를 넣을 때 사용
print('"hello" world')

